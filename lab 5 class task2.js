// String (Text data)
let name = "Jaweria";  
console.log("String:", name); // Output: String: Aleeza

// Number (Integer and Floating point)
let age = 25;  
let pi = 3.1416;  
console.log("Number:", age); // Output: Number: 25
console.log("Float Number:", pi); // Output: Float Number: 3.1416

// Boolean (true/false values)
let isStudent = true;  
let hasGraduated = false;  
console.log("Boolean:", isStudent); // Output: Boolean: true
console.log("Boolean:", hasGraduated); // Output: Boolean: false

// Null (Intentional absence of value)
let emptyValue = null;  
console.log("Null:", emptyValue); // Output: Null: null

// Undefined (Variable declared but not assigned a value)
let notAssigned;  
console.log("Undefined:", notAssigned); // Output: Undefined: undefined