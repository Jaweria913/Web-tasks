let a = 5;

console.log("Initial value of a:", a);

// Post-increment (a++)
console.log("Post-increment (a++):", a++); // Prints 5, then a becomes 6
console.log("Value after post-increment:", a); // Now a is 6

// Pre-increment (++a)
console.log("Pre-increment (++a):", ++a); // Increments first, then prints 7

// Post-decrement (a--)
console.log("Post-decrement (a--):", a--); // Prints 7, then a becomes 6
console.log("Value after post-decrement:", a); // Now a is 6

// Pre-decrement (--a)
console.log("Pre-decrement (--a):", --a); // Decrements first, then prints 5
